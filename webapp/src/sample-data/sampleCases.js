export const getStatusCounts = () => {
  const counts = {
    InvoiceApproved: 0,
    ReadyForApproval: 0,
    Failed: 0,
    Pending: 0,
  };

  sampleCases.forEach((caseItem) => {
    if (counts.hasOwnProperty(caseItem.status)) {
      counts[caseItem.status]++;
    } else {
      counts["Pending"]++;
    }
  });

  return counts;
};

export const searchCases = (query) => {
  if (!query) return sampleCases;

  const lowercaseQuery = query.toLowerCase();
  return sampleCases.filter(
    (caseItem) =>
      caseItem.vehicle?.vehicleLicenseNumber
        ?.toLowerCase()
        .includes(lowercaseQuery) ||
      caseItem.caseNumber?.toLowerCase().includes(lowercaseQuery) ||
      caseItem.insuranceInformation?.insuranceProvider?.name
        ?.toLowerCase()
        .includes(lowercaseQuery) ||
      caseItem.vehicle?.brandName?.toLowerCase().includes(lowercaseQuery) ||
      caseItem.vehicle?.model?.toLowerCase().includes(lowercaseQuery) ||
      caseItem.cause?.toLowerCase().includes(lowercaseQuery) ||
      caseItem.status?.toLowerCase().includes(lowercaseQuery) ||
      caseItem.vehicle?.vin?.toLowerCase().includes(lowercaseQuery) ||
      caseItem.legalOwner?.name?.toLowerCase().includes(lowercaseQuery) ||
      caseItem.contact?.name?.toLowerCase().includes(lowercaseQuery) ||
      caseItem.workshop?.name?.toLowerCase().includes(lowercaseQuery) ||
      // Search in order lines
      (caseItem.orderLines &&
        caseItem.orderLines.some(
          (orderLine) =>
            orderLine.priceAgreementResponse?.newCategory
              ?.toLowerCase()
              .includes(lowercaseQuery) ||
            orderLine.articleNumber?.toLowerCase().includes(lowercaseQuery)
        ))
  );
};

export const getCaseById = (id) => {
  return sampleCases.find((caseItem) => caseItem.id === id);
};

export const getCaseByCaseNumber = (caseNumber) => {
  return sampleCases.find((caseItem) => caseItem.caseNumber === caseNumber);
};
